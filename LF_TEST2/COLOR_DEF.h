#ifndef        __COLOR_DEF_H
#define        __COLOR_DEF_H
                                                                                        //      B         G         R
#define		COLOR_BLUE        0xF800			// 11111 000000 00000
#define		COLOR_GREEN        0x07E0			// 00000 111111 00000
#define		COLOR_RED		0x001F			// 00000 000000 11111

#define		COLOR_DGREEN	0x3F40			// 00001 111000 00000 RGB=0,153,68
#define		COLOR_BLACK		0x0000

#define		COLOR_ORANGE	0x009F			// 00000 000100 11100 RGB=128,128,0
#define		COLOR_WHITE		0xFFFF
#define		COLOR_DGRAY	0x0841			// 00001 000010 00001 RGB=160,160,160
#define		COLOR_GRAY		0xBDF7			// 10111 101111 10111 RGB=220,220,220
#define		COLOR_PINK		0xF81F			// 11111 000000 11111 RGB=255,0,255

#define		COLOR_CYAN		0xFFE0			// 11111 111111 00000
#define		COLOR_YELLOW	0x07FF			// 00000 111111 11111





#endif        // __COLOR_DEF_H

